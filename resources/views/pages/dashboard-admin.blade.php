@section('chart-js')
<script>
    //
        // Bars chart
        //
        var BarsChart = (function() {
            //
            // Variables
            //
            var $chart = $('#chart');
            var chartData = JSON.parse(`<?php echo $data_chart ?>`);
            //
            // Methods
            //
            // Init chart
            function initChart($chart) {
                // Create chart
                var ordersChart = new Chart($chart, {
                    type: 'bar',
                    data: {
                        labels:  chartData.label,
                        datasets: [{
                            label: 'Sales',
                            data: chartData.data
                        }]
                    }
                });
                // Save to jQuery object
                $chart.data('chart', ordersChart);
            }


            // Init chart
            if ($chart.length) {
                initChart($chart);
            }

        })();
</script>
@endsection